package curs1;

public class Tester {

    //variabile
    String nume;
    int ratePerHour;
    int Age;

    //metode
    public void setNume(String nume){
        this.nume = nume;
    }

    public String getNume(){
        return nume;
    }

    public void setRatePerHour(int ratePerHour){
        this.ratePerHour = ratePerHour;
    }

    public int getRatePerHour(){
        return ratePerHour;
    }

    public void setAge(int Age) {
        this.Age = Age;
    }

    public int getAge(){
        return Age;
    }
}
